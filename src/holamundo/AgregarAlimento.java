    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Vector<String> ingredientes=new Vector<String>();
        Vector<String> enfermedades=new Vector<String>();
         Vector<String> regiones=new Vector<String>();
        String nombre=jTextField1.getText();
        String ingredientesI=jTextField2.getText();
        StringTokenizer tkn1=new StringTokenizer(ingredientesI,",");
        for(int i=0;i<tkn1.countTokens();i++)
        {
            String aux=tkn1.nextToken().replaceAll(" ", "");
            ingredientes.add(aux);
        }
        String enfermedadesI=jTextField3.getText();
        StringTokenizer tkn2=new StringTokenizer(enfermedadesI,",");
        for(int i=0;i<tkn2.countTokens();i++)
        {
            String aux=tkn2.nextToken().replaceAll(" ", "");
            enfermedades.add(aux);
        }
        if(jCheckBox1.isSelected())
            regiones.add("costa");
        if(jCheckBox2.isSelected())
            regiones.add("selva");
        if(jCheckBox3.isSelected())
            regiones.add("sierra");
        
        for(int i=0;i<ingredientes.size();i++)
	{
            String pro="asserta(ingred("+ingredientes.elementAt(i)+","+nombre+"))";
            Query q1 = new Query(pro);
            System.out.println( q1 + " " + (q1.hasSolution() ? "Bien" : "Mal") );
	}
        
        for(int i=0;i<enfermedades.size();i++)
	{
            String pro1="asserta(bueno_contra("+nombre+","+enfermedades.elementAt(i)+"))";
            Query q12 = new Query(pro1);
            System.out.println( q12 + " " + (q12.hasSolution() ? "Bien" : "Mal") );
	}
        
        for(int i=0;i<regiones.size();i++)
	{
            String pro1="asserta(region("+regiones.elementAt(i)+","+nombre+"))";
            Query q12 = new Query(pro1);
            System.out.println( q12 + " " + (q12.hasSolution() ? "Bien" : "Mal") );
	}
        
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

